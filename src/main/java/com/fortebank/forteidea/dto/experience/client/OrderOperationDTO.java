package com.fortebank.forteidea.dto.experience.client;

public class OrderOperationDTO {
    private Long id;
    private String name;

    public OrderOperationDTO(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
